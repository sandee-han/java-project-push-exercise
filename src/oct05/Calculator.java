package oct05;

public class Calculator {
    public int plus(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public double div(double a, double b) {
        return (a / b);
    }

    public int multiple(int a, int b) {
        return a * b;
    }
}
